package fr.ipac.multigame.model;

public class Game {
    public Game(String name, Integer score){
        setName(name);
        setScore(score);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getScore() {
        return score;
    }

    public void setScore(Integer score) {
        this.score = score;
    }

    String name;
    Integer score;


}
